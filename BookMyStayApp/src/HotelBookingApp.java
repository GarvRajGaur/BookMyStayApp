/**
 * HotelBookingApplication
 *
 * Entry point for the Hotel Booking System.
 * This class demonstrates how a Java application begins execution,
 * prints startup information to the console, and terminates.
 *
 * The application establishes a predictable startup behavior
 * for future system expansion.
 *
 * @author Garv Raj
 * @version 1.0
 */
public class HotelBookingApp {

    /**
     * Main method – application entry point.
     * The JVM invokes this method to start execution.
     *
     * @param args command-line arguments (not used in this version)
     */
    public static void main(String[] args) {

        // Print welcome message
        System.out.println("Welcome to the Hotel Booking System");
        System.out.println("Application: Hotel Booking System");
        System.out.println("Version: 1.0");

        // Application terminates naturally after execution completes
    }
}